#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	s_len;
	size_t	i;

	if (s == NULL)
		return ;
	s_len = ft_strlen(s);
	i = 0;
	while (i < s_len)
	{
		s[i] = '\0';
		i++;
	}
}
